package content;

public class Student {
    
    
    private String name = new String();
    private int sid=0;
    
    
    public void setName(String name){
        this.name = name;
    }
    
    public void setSid(int sid){
        this.sid = sid;
    }

    /**
     * @return the name
     */
    public String getName() {
        return this.name;
    }

    /**
     * @return the sid
     */
    public int getSid() {
        return this.sid;
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
}
